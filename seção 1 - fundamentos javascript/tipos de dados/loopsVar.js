
/*incremento e decrimento
a) incremento = quando aumenta
sintaxe: i++ || i+=1 || i = i + 1

b) decrimento = quando diminui
sintaxe: i-- || i-=1 || i = i - 1*/

console.log("Repetição com incremento")
for (var i = 0; i < 10; i++) {
    console.log(i)
}


console.log("\nRepetição com decrimento")
for (var i = 10; i > 0; i--) {
    console.log(i)
}

